package cn.zys.bean;

import java.io.Serializable;

public class Person implements Serializable{

	private Integer id;
	private String name;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}